
public class Position {
	private int xPos;
	private int yPos;
	
	public Position() {
		this.xPos = 0;
		this.yPos = 0;
	}
	
	public void setXPos(int i) {this.xPos = i;}
	public void setYPos(int i) {this.yPos = i;}
	
	public int getXPos() {return this.xPos;}
	public int getYPos() {return this.yPos;}

}
